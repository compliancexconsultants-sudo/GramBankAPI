require("dotenv").config();
const express = require("express");
const mongoose = require("mongoose");
const cors = require("cors");
const bodyParser = require("body-parser");

const userRoutes = require("./routes/userRoutes");
const txnRoutes = require("./routes/txnRoutes");

const app = express();

// âœ… Universal CORS Setup (Compatible with Express 5)
app.use(
  cors({
    origin: "*", // Allow all origins
    methods: ["GET", "POST", "PUT", "DELETE", "OPTIONS"],
    allowedHeaders: ["Content-Type", "Authorization"],
  })
);

// âœ… Safe alternative to avoid path-to-regexp '*' error
app.use((req, res, next) => {
  res.header("Access-Control-Allow-Origin", "*");
  res.header(
    "Access-Control-Allow-Headers",
    "Origin, X-Requested-With, Content-Type, Accept, Authorization"
  );
  res.header("Access-Control-Allow-Methods", "GET, POST, PUT, DELETE, OPTIONS");
  if (req.method === "OPTIONS") return res.sendStatus(200);
  next();
});

// Middleware
app.use(bodyParser.json());
app.use(express.json());

// âœ… MongoDB connection
mongoose
  .connect(process.env.MONGO_URI)
  .then(() => console.log("âœ… MongoDB Connected"))
  .catch((err) => console.error("âŒ MongoDB Connection Error:", err));

// âœ… Routes
app.use("/api/users", userRoutes);
app.use("/api/txns", txnRoutes);

// âœ… Default route
app.get("/", (req, res) => {
  res.json({ message: "GramBank API is running ğŸš€" });
});

// âœ… Start server
const PORT = process.env.PORT || 5000;
app.listen(PORT, () => console.log(`ğŸš€ Server running on port ${PORT}`));
